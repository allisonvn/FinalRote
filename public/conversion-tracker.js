/**
 * üéØ ROTAFINAL - RASTREADOR AUTOM√ÅTICO DE CONVERS√ïES (v2.0)
 * 
 * Este script detecta automaticamente quando a p√°gina de sucesso √© acessada
 * e registra a convers√£o no Supabase com o valor configurado.
 * 
 * COMO USAR:
 * 
 * 1. Adicione este script na p√°gina de sucesso/convers√£o:
 *    <script src="https://rotafinal.com.br/conversion-tracker.js"></script>
 * 
 * 2. Configure a URL de convers√£o no experimento (Etapa 3 do modal)
 * 
 * 3. Quando o usu√°rio acessar a p√°gina de sucesso, a convers√£o ser√°
 *    automaticamente registrada no Supabase com o valor configurado
 * 
 * O script detecta:
 * - Qual experimento est√° ativo (via localStorage)
 * - Qual variante foi atribu√≠da ao visitante
 * - O valor de convers√£o configurado no experimento
 * - Registra tudo no Supabase automaticamente
 */

(function() {
  'use strict';

  // Configura√ß√µes
  const CONFIG = {
    apiUrl: 'https://rotafinal.com.br/api/track',
    batchApiUrl: 'https://rotafinal.com.br/api/track/batch',
    debug: typeof window !== 'undefined' && window.location.hostname === 'localhost',
    maxWaitTime: 5000, // 5 segundos max para enviar
  };

  // Fun√ß√£o de log apenas em modo debug
  function log(...args) {
    if (CONFIG.debug) {
      console.log('üéØ [ConversionTracker]', ...args);
    }
  }

  /**
   * Buscar dados de atribui√ß√£o de variante do localStorage
   */
  function getAssignmentData() {
    try {
      log('üîç Procurando dados de atribui√ß√£o no localStorage');
      
      const storageKeys = Object.keys(localStorage);
      
      // Procurar por chaves rotafinal_* (incluindo as novas chaves do SDK inline)
      for (const key of storageKeys) {
        if (key.startsWith('rotafinal_exp_') || key.startsWith('rotafinal_assignment_') || key.startsWith('rf_experiment_')) {
          try {
            const data = JSON.parse(localStorage.getItem(key));
            log('‚úÖ Dados encontrados:', { key, data });
            
            // Se for dados do experimento (rf_experiment_), extrair dados da variante tamb√©m
            if (key.startsWith('rf_experiment_')) {
              const experimentId = key.replace('rf_experiment_', '');
              const variantKey = `rf_variant_${experimentId}`;
              const variantData = localStorage.getItem(variantKey);
              
              if (variantData) {
                try {
                  const variant = JSON.parse(variantData);
                  return {
                    key,
                    experimentId: experimentId,
                    variantId: variant.v?.id || variant.id,
                    variantName: variant.v?.name || variant.name,
                    visitorId: localStorage.getItem('rf_user_id'),
                    timestamp: variant.t || Date.now()
                  };
                } catch (e) {
                  log('‚ö†Ô∏è Erro ao parsear dados da variante:', variantKey, e);
                }
              }
            } else {
              // Dados diretos de atribui√ß√£o
              return {
                key,
                experimentId: data.experimentId || data.experiment_id,
                variantId: data.variantId || data.variant_id,
                variantName: data.variant || data.variantName,
                visitorId: data.visitorId || data.visitor_id,
                timestamp: data.timestamp
              };
            }
          } catch (e) {
            log('‚ö†Ô∏è Erro ao parsear dados:', key, e);
          }
        }
      }
      
      log('‚ö†Ô∏è Nenhum experimento ativo encontrado no localStorage');
      return null;
    } catch (error) {
      log('‚ùå Erro ao buscar dados de atribui√ß√£o:', error);
      return null;
    }
  }

  /**
   * Buscar dados do experimento da API (para obter o valor de convers√£o)
   */
  async function getExperimentData(experimentId) {
    try {
      log('üì° Buscando dados do experimento:', experimentId);
      
      const response = await fetch(`https://rotafinal.com.br/api/experiments/${experimentId}`, {
        method: 'GET',
        headers: {
          'Content-Type': 'application/json',
        }
      });

      if (!response.ok) {
        throw new Error(`HTTP error! status: ${response.status}`);
      }

      const result = await response.json();
      log('‚úÖ Dados do experimento obtidos:', result);
      
      return {
        conversionValue: result.experiment?.conversion_value || 0,
        conversionUrl: result.experiment?.conversion_url,
        conversionType: result.experiment?.conversion_type || 'page_view'
      };
    } catch (error) {
      log('‚ö†Ô∏è Erro ao buscar dados do experimento:', error);
      return {
        conversionValue: 0,
        conversionUrl: null,
        conversionType: 'page_view'
      };
    }
  }

  /**
   * Registrar convers√£o na API
   */
  async function trackConversion(assignmentData, experimentData) {
    try {
      log('üìä Registrando convers√£o:', {
        experimentId: assignmentData.experimentId,
        variantId: assignmentData.variantId,
        value: experimentData.conversionValue
      });

      // Verificar se j√° converteu (evitar duplicatas)
      const conversionKey = `rotafinal_conversion_${assignmentData.experimentId}`;
      if (localStorage.getItem(conversionKey)) {
        log('‚úÖ Convers√£o j√° registrada anteriormente para este experimento');
        return true; // Retornar sucesso para n√£o tentar novamente
      }

      // ‚úÖ CORRE√á√ÉO: Buscar dados da p√°gina de origem do localStorage
      const originPageData = JSON.parse(
        localStorage.getItem(`rotafinal_origin_${assignmentData.experimentId}`) || '{}'
      );

      // Preparar dados da convers√£o COMPLETOS
      const conversionPayload = {
        experiment_id: assignmentData.experimentId,
        visitor_id: assignmentData.visitorId,
        variant_id: assignmentData.variantId,
        variant: assignmentData.variantName,
        event_type: 'conversion',
        event_name: 'conversion',
        value: experimentData.conversionValue || 0,
        timestamp: new Date().toISOString(),
        properties: {
          // ‚úÖ P√°gina de sucesso (atual)
          success_page_url: window.location.href,
          success_page_title: document.title,
          
          // ‚úÖ P√°gina que originou a convers√£o (do localStorage)
          origin_page_url: originPageData.url || experimentData.targetUrl,
          origin_page_title: originPageData.title || 'P√°gina de Origem',
          
          // ‚úÖ Dados de navega√ß√£o
          referrer: document.referrer,
          user_agent: navigator.userAgent,
          viewport: {
            width: window.innerWidth,
            height: window.innerHeight
          },
          
          // ‚úÖ Dados da convers√£o
          conversion_value: experimentData.conversionValue,
          conversion_type: experimentData.conversionType,
          timestamp: new Date().toISOString(),
          success_page: true,
          
          // ‚úÖ Dados da variante
          variant: assignmentData.variantName,
          variant_id: assignmentData.variantId
        }
      };

      log('üì§ Enviando convers√£o para API:', conversionPayload);

      // Enviar para API
      const response = await fetch(CONFIG.apiUrl, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'X-Requested-With': 'XMLHttpRequest'
        },
        body: JSON.stringify(conversionPayload)
      });

      if (!response.ok) {
        throw new Error(`HTTP error! status: ${response.status}`);
      }

      const result = await response.json();
      log('‚úÖ Convers√£o registrada com sucesso:', result);

      // Marcar como convertido no localStorage
      localStorage.setItem(conversionKey, JSON.stringify({
        converted_at: new Date().toISOString(),
        experiment_id: assignmentData.experimentId,
        variant: assignmentData.variantName,
        value: experimentData.conversionValue
      }));

      // Disparar evento customizado
      if (window.dispatchEvent) {
        const event = new CustomEvent('rotafinal_conversion', {
          detail: {
            experimentId: assignmentData.experimentId,
            variantId: assignmentData.variantId,
            value: experimentData.conversionValue
          }
        });
        window.dispatchEvent(event);
        log('üéâ Evento de convers√£o disparado');
      }

      return true;
    } catch (error) {
      log('‚ùå Erro ao registrar convers√£o:', error);
      return false;
    }
  }

  /**
   * Inicializar rastreamento
   */
  async function init() {
    log('üöÄ Iniciando ConversionTracker');
    log('üìç P√°gina atual:', window.location.href);

    // Buscar dados de atribui√ß√£o
    const assignmentData = getAssignmentData();
    
    if (!assignmentData) {
      log('‚ö†Ô∏è Nenhuma atribui√ß√£o de variante encontrada. SDK pode n√£o ter sido executado ou visitante n√£o participante.');
      return;
    }

    log('‚úÖ Dados de atribui√ß√£o encontrados:', assignmentData);

    // Buscar dados do experimento (incluindo valor de convers√£o)
    const experimentData = await getExperimentData(assignmentData.experimentId);
    
    log('‚úÖ Dados do experimento:', experimentData);

    // Registrar convers√£o
    const success = await trackConversion(assignmentData, experimentData);
    
    if (success) {
      log('üéä Convers√£o rastreada com sucesso!');
    } else {
      log('‚ö†Ô∏è Falha ao rastrear convers√£o, tentando novamente em 2 segundos...');
      // Tentar novamente uma vez
      setTimeout(async () => {
        await trackConversion(assignmentData, experimentData);
      }, 2000);
    }
  }

  // Esperar DOM estar pronto
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    // DOM j√° est√° pronto
    setTimeout(init, 100);
  }

  // Expor m√©todos globais para testes/debug
  if (typeof window !== 'undefined') {
    window.RotaFinalConversionTracker = {
      debug: () => {
        CONFIG.debug = true;
        log('‚úÖ Debug ativado');
      },
      test: async () => {
        log('üß™ Teste manual iniciado');
        const assignmentData = getAssignmentData();
        if (assignmentData) {
          const experimentData = await getExperimentData(assignmentData.experimentId);
          console.table({ assignmentData, experimentData });
        } else {
          log('‚ùå Sem dados de atribui√ß√£o. Teste o SDK primeiro.');
        }
      }
    };
  }
})();

